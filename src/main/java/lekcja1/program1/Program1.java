package lekcja1.program1;

/**
 * Klasa wyswietla rozne typy zmiennych
 * Author: Amina
 */
public class Program1 {

    public static void main(String[] args) {
        int zmiennaInt = 8;                       // zmienna typu int (typ liczb calkowitych)
        double zmiennaDouble = 3.2;               // zmienna typu double (typ liczb zmiennoprzecinkowych)
        float zmiennaFloat = 4.65f;               // zmienna typu float (typ liczb zmiennoprzecinkowych)
        String zmiennaString = "To jest napis";   // zmienna typu String (typ tekstowy)
        boolean zmiennaBoolean = true;            // zmienna typu boolean (typ logiczny)

        System.out.println("zmiennaInt = " + zmiennaInt);
        System.out.println("zmiennaDouble = " + zmiennaDouble);
        System.out.println("zmiennaFloat = " + zmiennaFloat);
        System.out.println("zmiennaString = " + zmiennaString);
        System.out.println("zmiennaBoolean = " + zmiennaBoolean);
    }

}
